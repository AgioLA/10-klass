#Это первая программа на Python в репозитории
#Эта программа поможет вам вычислить квадратный корень из любого числа
#Попробуйте ее усовершенствовать, например, добавив обработку исключений


import time
while True:
    number = input("Введите ваше число: ")
    try:
        a = int(number)
    except ValueError as error1:
        print('Ошибка! повторите ввод. Исключение:, error1')
        continue
    b = a ** 0.5
    print('Квадратный корень числа', a, 'равен', b)
    time.sleep(5) #Задержка после выполнения программы
    break
